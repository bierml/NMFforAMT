def sparsity(x, eps=1e-10):
    x = np.asarray(x).ravel()
    N = x.size

    if N <= 1:
        return 0.0  # not defined, fallback

    l2_norm = np.sqrt(np.sum(x**2)) + eps

    term = np.sum(np.tanh(np.abs(x)**2)) / l2_norm

    return (np.sqrt(N) - term) / (np.sqrt(N) - 1)

