def polyphony_autocorr(V, max_lag=200, threshold=0.3):
    F, T = V.shape
    P = np.zeros(T)

    for t in range(T):
        v = V[:, t]
        v = v / (np.max(v) + 1e-10)

        # autocorrelation (positive lags only)
        R = np.correlate(v, v, mode='full')[F-1:F-1+max_lag]

        # normalize
        R /= np.max(R) + 1e-10

        # peak counting
        peaks = (R[1:-1] > R[:-2]) & (R[1:-1] > R[2:])
        peaks = peaks & (R[1:-1] > threshold)

        P[t] = np.sum(peaks)

    return P


